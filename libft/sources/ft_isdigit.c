/******************************************************************************
 *
 * booleen indiquant si le caractere passe en argument est un chiffre
 * renvoie 1 si vrai, 0 si faux
 *
 *****************************************************************************/
int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}
